          ->leftJoin('e.article','art' )
          ->where('art.event IS NULL')
        ;
        if (isset($_REQUEST['id']) && $_REQUEST['id']) {
            $repo
              ->orWhere('art.id =:article')
              ->setParameter('article',$_REQUEST['id'] );
        }
        return $repo;
